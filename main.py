def speak(text):
    tts = gTTS(text)
    tts.save('temp.mp3') 

    # Initialize Pygame mixer
    pygame.mixer.init()

    # Load the MP3 file
    pygame.mixer.music.load('temp.mp3')

    # Play the MP3 file
    pygame.mixer.music.play()

    # Keep the program running until the music stops playing
    while pygame.mixer.music.get_busy():
        pygame.time.Clock().tick(10)
    
    pygame.mixer.music.unload()
    os.remove("temp.mp3") 

def processCommand(c):
    if "open google" in c.lower():
        webbrowser.open("https://google.com")
    elif "open facebook" in c.lower():
        webbrowser.open("https://facebook.com")
    elif "open youtube" in c.lower():
        webbrowser.open("https://youtube.com")
    elif "open linkedin" in c.lower():
        webbrowser.open("https://linkedin.com")
    elif c.lower().startswith("play"):
        song = c.lower().split(" ")[1]
        link = musicLibrary.music[song]
        webbrowser.open(link)

    elif "news" in c.lower():
        api_key = "<YOUR_GNEWS_API_KEY>"

        url = f"https://gnews.io/api/v4/top-headlines?country=in&max=5&lang=en&apikey={api_key}"

        try:
            r = requests.get(url)
            print("STATUS:", r.status_code)
            print("RESPONSE:", r.text)

            if r.status_code == 200:
                data = r.json()
                articles = data.get("articles", [])

                if not articles:
                    speak("No news available right now.")
                else:
                    speak("Here are the latest headlines.")
                    for article in articles:
                        speak(article["title"])
            else:
                speak("Unable to fetch news right now.")
        except Exception as e:
            print("News error:", e)
            speak("There is a problem fetching news.")


   
